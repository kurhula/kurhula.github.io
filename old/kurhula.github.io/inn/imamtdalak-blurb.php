    <h1 class="title-post"><a href="inn/imamtdalak.php">Introducing Madyondza</a></h1>

    <h2 class="title-post">The Johannesburg meetup with the goal of localising world information and
        digitising African knowledge</h2>

    <p class="date">Last updated on: September 30, 2014 by <a href="../index.php#about">Musa Kurhula Baloyi</a></p>

    <p>
        <strong>The name</strong>
    </p>

    <p>
        The name “madyondza” is a Xitsonga word for “first child”. Split into “ma” and “dyondza” it would
        mean: “you learn”. These two meanings are consistent with a project of this kind. This is why:
    </p>

    <ol>
        <li>
            It is an experiment
        </li>
        <li>
            It is educational
        </li>
    </ol>

    <p>
        Over and above these reasons, I chose it because one can explain a lot of things about language and
        grammar from it. It has a synonym, it follows a particular orthography, and like most other words,
        spelling it wrong can change the meaning of your sentence.
    </p>

    <p>
        <strong>The mission</strong>
    </p>

    <p>
        Localisation and digitisation in the African context leaves much to be desired. In the past, this
        topic has been relegated to blog discussions and WhatsApp group chats. This way of doing things has
        proved inefficient. Too many disparate groups have emerged, albeit with a single vision.
    </p>

    <p>
        We hope to connect people with different skills, belonging to varying organisations, to achieve a
        single goal. Part of it is to also help each other understand what this single goal actually is, as
        well as work on refining it and keeping it alive.
    </p>

