                <h1 class="title-post">Introducing Madyondza</h1>
                <h2 class="title-post">The Johannesburg meetup with the goal of localising world information and
                    digitising African knowledge</h2>

                <p class="date">Last updated on: September 30, 2014 by <a href="../index.php#about">Musa Kurhula Baloyi</a></p>

                <p>
                    <strong>The name</strong>
                </p>

                <p>
                    The name “madyondza” is a Xitsonga word for “first child”. Split into “ma” and “dyondza” it would
                    mean: “you learn”. These two meanings are consistent with a project of this kind. This is why:
                </p>

                <ol>
                    <li>
                        It is an experiment
                    </li>
                    <li>
                        It is educational
                    </li>
                </ol>

                <p>
                    Over and above these reasons, I chose it because one can explain a lot of things about language and
                    grammar from it. It has a synonym, it follows a particular orthography, and like most other words,
                    spelling it wrong can change the meaning of your sentence.
                </p>

                <p>
                    <strong>The mission</strong>
                </p>

                <p>
                    Localisation and digitisation in the African context leaves much to be desired. In the past, this
                    topic has been relegated to blog discussions and WhatsApp group chats. This way of doing things has
                    proved inefficient. Too many disparate groups have emerged, albeit with a single vision.
                </p>

                <p>
                    We hope to connect people with different skills, belonging to varying organisations, to achieve a
                    single goal. Part of it is to also help each other understand what this single goal actually is, as
                    well as work on refining it and keeping it alive.
                </p>

                <img src="../img/Madyondza.png" class="img-responsive">
                <br/>

                <p>
                   <strong>The people</strong>
                </p>

                <p>
                    Madyondza brings together people from various backgrounds and occupations. The regular crowd is
                    divided into three clusters:
                </p>

                <ol>
                    <li>
                        Technologists
                    </li>
                    <li>
                        Practitioners
                    </li>
                    <li>
                        Stakeholders
                    </li>
                </ol>

                <p>
                    By technologists we are referring to developers, designers, analysts and testers. They look at the
                    technical requirements of having the Madyondza website and related applications up and running.
                </p>

                <p>
                    Practitioners are those who work on a daily basis with the issues we entertain. These could be
                    linguists, translators, writers and other artists, archivists, academics, researchers or just about
                    anyone who is immersed in an area of interest.
                </p>

                <p>
                    Stakeholders have vested interests in seeing the project or a portion of it succeed. We are talking
                    entrepreneurs who could get business ideas from our work, a government that wants indigenous
                    languages advance, sponsors and users of the uploaded content.
                </p>

                <p>
                    <strong>The events</strong>
                </p>

                <p>
                    We intend to have Madyondza engagements on a monthly basis, however, the involvement is ongoing. The
                    event takes the form of a hackathon punctuated with talks, discussions, and good old African meals.
                    The music, drinks and light conversations at the end of the day become the full-stop.
                </p>

                <p>
                    One of the aims is to have an MVP (most valuable product) at the end of the 7-hour day. It is hoped
                    that having an MVP will be motivational as we are able to see the fruits of our sweat immediately.
                    Instead of building a new site each time, we will use a microservice architecture where new complete
                    components can be added or removed, allowing the user to decide what they want to see on their
                    dashboard.
                </p>

                <p>
                    In the build up to a particular meetup, we collaboratively decide on a theme. Each theme has its own
                    requirements so we set up the machines and collect data to make it easier to do something useful.
                </p>

                <p>
                    Our first meetup was on the 27 of September 2014 at the ThoughtWorks South Africa offices. We worked
                    on visualising land ownership data over time. For more information, please click
                    https://www.meetup.com/madyondza/events/205891002/.
                </p>

                <img src="../img/Madyondza2.png" class="img-responsive">
                <br/>

                <p>
                    <strong>The outcome</strong>
                </p>

                <p>
                    Our first Madyondza event attracted 12 keen participants, both students and professionals. The
                    students were in the fields of law and journalism. The professionals were developers, a designer, a
                    business analyst and a management consultant. Additionally, there were aspiring entrepreneurs.
                    Sadly, we did not have any testers.
                </p>

                <p>
                    What came out of the retrospective was that the participants were actively involved, there were
                    varying skills, it was an open discussion forum and the traditional African food was scrumptious!
                </p>

                <p>
                    The three ThoughtWorkers involved in the retrospective were worried about not entirely meeting our
                    meetup goal because of a shortage of technical attendants and the lack of a clearly defined purpose
                    for the meetup.
                </p>

                <p>
                    For the next meetup, we need to be smarter about how to pair participants who may have varying
                    skills. We will also have to think about how to keep the previous meetup’s resolutions and codebase
                    alive. The likely topic is “Digitising the English-Tsonga/Tsonga-English Pocket Dictionary” as a
                    prelude to digitising other African language pocket dictionaries. We hope you can join us! And bring
                    your tester friends.
                </p>

