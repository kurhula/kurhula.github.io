                <h1 class="title-post">Gumbani-Mukhomi-Phaphazela</h1>

                <p class="date">Last updated on: September, 2010 by <a href="../index.php#about">Bantsi Makhandeni</a>
                </p>
                <br/>

                <p>
                    On 10 September 2010, myself and my mum (mhani) took the Do Light/ Swangi's bus from Gumbani to
                    Mukhomi in Malamulele, South Africa. We took it from the bus stop facing Khatisa High School. I
                    tried to run towards it after the driver hit the brakes so I could tell him to wait for mhani. But
                    mhani said “There are a lot of people about to get on. Don't worry, we'll get it.” I couldn't miss
                    this bus because I had a very important meeting to attend.
                    I tell the driver exactly where we're going. At first he pretended not to hear, but I repeated my
                    words without alter except in emphasis. We paid R10 for two persons. I was hoping for change but
                    after seeing the receipt, we took a seat.
                </p>

                <p>
                    Before we got to Humula High School, on the road branching off to Phaphazela, he turns left. And so
                    I charge to the front, stopping only by the sign that read "Shumela Venda" (Working for Venda). I
                    said to him with emotion “But we said we were going to Mukhomi, this is not it!”
                    In response, he gave me R2. I looked at it with disbelief and said “We were two!” He said “I know,”
                    calmly. My breathing started to change because his wasn't. On top of that, I was with mhani! I
                    wouldn't care what he did to me, but mhani! He knew what he had done was wrong but the man had no
                    remorse.
                </p>

                <p>
                    As a result myself and mhani had to walk upslope all the way to the Tribal Authorities, not to
                    report him but for other reasons. That was taken care of because the bus-taxi wars always led them
                    to her place of work. They would meet again.
                </p>

                <p>
                    Those who know Mukhomi know that it's about 1 kilometer from Vonani (where Humula is) to ka Xikhulu
                    (where the Tribal Authorities are). We were charged R4 each from Khatisa to Humula, a distance of
                    about 1 km. I walked to Humula from deep inside Gumbani for 4 years without seeing the need to use
                    other means of transport. Mhani did the same before me. That's how not far it was.
                </p>

                <p>
                    R2. 2 people. 1 kilometer. What taxi or bus could we take?
                </p>

                <p>
                    I told mhani I had noticed something was wrong about that driver from first contact. My first
                    instincts told me he was a retard. But he was not. “Is he Venda? Was he expecting me to speak in
                    Venda?” To all this she could only give a nod. If you're serving a people, it's best you observe
                    their culture, rituals and most importantly their language. Not this man. He was here to impose.
                </p>

                <p>
                    I knew what was happening in my hometown and suspected this to be part of it. If you don't know,
                    some Venda people are trying to take over Malamulele. They succeeded with the municipality and now
                    they were advancing to Phase 2 or Phase 3 or whatever Phase they were on. This new Phase included
                    some Venda people reclaiming land in Malamulele as their own while Tsonga people are being
                    compensated (for those who were lucky) with cash or housing. Cash can be spent, housing will be
                    dilapidated, land is an everlasting investment. Since they've taken the municipality, they've taken
                    our records. They can make whatever claims and we have no proof.
                </p>

                <p>
                    Thinking back. On the bus were two of my high school teachers, both of the Venda nation. Mhani drew
                    my attention to them. I shook one teacher's hand because he was closer and he had taught me in
                    school. We have lived with these people without reservations, but now my mind was thinking “could
                    they be part of this coup?” If so, I would challenge them, just like old times. They know me
                    intimately, so do I. It would boil down to chance.
                </p>

                <p>
                    When we got to the Tribal Authorities (T/A), the Chief (hosi) had not come back from a meeting ka
                    Malamulele. It was a shock they still had meetings in this town. We waited under one of the trees
                    there. Two male cops were seating.
                </p>

                <p>
                    Another mishappening came to the light outside the offices. Local business-women were complaining
                    that they had loaned money to the contract workers there but because today they were getting paid,
                    they were nowhere to be seen. One was being owed exactly R1820 by one person. It was all documented,
                    with dates, amounts and reasons. Likewise, these contract workers came from the other side of the
                    river. What about the local workforce?
                </p>

                <p>
                    The cops there asked why one would loan such an amount to a contract worker, knowing he doesn't even
                    earn that much. She said he was desperate to go home for a funeral and on other occasions he needed
                    ti-bakfish to eat. She couldn't let him starve, a display of vumunhu.
                </p>

                <p>
                    Others were being owed less money like R100. The debtor, one of the two who were there, wouldn't
                    cough up. He said in his native, “R100 is not much money, why won't you leave me alone!” She said in
                    her native, “If R100 is not much money, why won't you just give it to me?”
                </p>

                <p>
                    The Chief arrived, and calmly he spoke. “The contractor couldn't give another man's money to a
                    machonisi (creditor) because she was not the one who worked for it. The contractor is only obligated
                    to give that money to the contract worker, and if he didn't show up, the contractor would have to
                    pay him another way.”
                </p>

                <p>
                    The R100 man, after saying "Machangana a ya dina", (Shangaans are irritating) finally gave the woman
                    her money. She took it gracefully and without unnecessary remarks or disgruntled facial expressions,
                    she went on her way. This to me was an indication of what it means to say “Mutsonga wo chava ku
                    tsongola swa van'wana." (A Tsonga person who does not take from others.) I could extend it to say
                    “kambe swa yena wa swi lava” (but he will take what belongs to him).
                </p>

                <p>
                    We met with the Chief for a brief period and on our way to catch the taxi back (well I was the one
                    going back, mhani was on her way to the gym in Thohoyandou), and on seeing 'Van Davies Tombstones' I
                    asked “Is this Davies as in buti Davies va ka Ash?” She replied “No. It's a Venda man called Davies.
                    He calls himself that. He's such a hardworker! They make tombstones and bricks. It's the new
                    lucrative business these days this side of town.” And then we went on talking business.
                </p>
