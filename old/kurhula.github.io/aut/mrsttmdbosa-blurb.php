    <p class="date">Last updated on: March 04, 2015 by <a href="https://www.facebook.com/nkateko.mutsonga">Nkateko
            Mirhumba Chake</a></p>


    <p>
        Nhlanganelo wo Hluvukisa Xitsonga (NHLAHLUXI) is a registered NPO whose objective is to promote the
        development of all Vatsonga in the Southern African subcontinent (region), and indeed the whole
        world.
    </p>

    <p>
        We have acknowledged with concern the recent developments in the Malamulele area and we feel that we
        cannot remain aloof when the rights of our people are trampled upon and the will of the affected
        people, not the wishes of their political masters that are paramount in determining the discourse
        and direction to be taken. Indeed, after making their grievances known, the people of Malamulele
        must surely be wondering about the following:

    <ol>
        <li>
            Who in fact is governing their area if they, the people and their wishes are not taken into
            consideration when the decision affecting their daily lives are taken?
        </li>
        <li>
            Who represents them and their interests when in the decision making process after realizing that
            the voices of their elected representatives (e.g. Task Team) are either muted, ignored or absent
            in this whole demarcation upheaval.
        </li>
        <li>
            Whether they are taken seriously since successive delegations representing local, regional
            (district), provincial and national government structures have visited the area whenever
            tensions flare up and yet each contingent seems to be on a new fact-finding mission every time,
            meaning either the previous ones never reported or it was a window-dressing exercise.
        </li>
    </ol>
