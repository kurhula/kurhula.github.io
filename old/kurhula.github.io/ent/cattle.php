                <p><strong>Purchase</strong></p>

                When we bought the first few cows we had not built a kraal, so the shepherd had to keep them at a
                different location. This was strenuous for us because we could not frequently check on them to ensure
                that all was well. The other stressful thing was that we could not readily recognise them while sharing
                a kraal with others that were not our own. The other fear was that they might acclimatize to this kraal
                and have orientation issues when moved for the second time (the first was when we bought them).
                <center><img src="../img/ent/gaza.JPG" class="img-responsive"></center>
                <br/>

                <p><strong>Preparing the Kraal</strong></p>
                We soon built a kraal and brought the cattle closet to the house. This way we are aware of everything
                that happens to them.
                <p><strong>Purchase and Preparing the Kraal</strong></p>
                <center><img src="../img/ent/xivala.jpg" class="img-responsive"></center>
                <br/>

                <p><strong>Drought and Related Challenges</strong></p>
                2016 has been afflicted with drought. Our cattle were not eating well as a result. Government relief was
                nowhere to be found even after inquiring. It turned out that the feed was not free and quantities one
                farmer could buy was limited. We resolved to stocking our own, which was not exactly cheap but was
                necessary to supplement inadequate grazing.
                <center><img src="../img/ent/dyandza.JPG" class="img-responsive"></center>
                <br/>

                <p><strong>Production</strong></p>
                Eventually we got healthy calves and the herd grew by about 50 percent within 3 years. We have had to
                cull one cow that fell into a ditch and broke its leg. At this point we were confronted with the
                question of what to do with the meat since we had not anticipated selling. We were not able to sell
                enough on time such that we had to donate and consume some of it. The idea was to use the money from the
                sales to buy a new cow as replacement for the loss. Unfortunately we could not meet the financial
                target. We hope to handle this situation better in future in case it recurs.
                <center><img src="../img/ent/rhole.JPG" class="img-responsive"></center>
                <br/>

                <p><strong>Herd Strength</strong></p>
                Currently things are going well for our heard of 15. From the calves we were able to get a bull too! We
                continue to think of ways to better manage the processes as well as make a profit for our efforts.
                <center><img src="../img/ent/tihomu1.JPG" class="img-responsive"></center>
