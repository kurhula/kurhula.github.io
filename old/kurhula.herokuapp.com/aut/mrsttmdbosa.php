                <h1 class="title-post">Media release statement to the Municipal Demarcation Board of South Africa</h1>

                <p class="date">Last updated on: March 04, 2015 by <a href="https://www.facebook.com/nkateko.mutsonga">Nkateko
                        Mirhumba Chake</a></p>


                <p>
                    Nhlanganelo wo Hluvukisa Xitsonga (NHLAHLUXI) is a registered NPO whose objective is to promote the
                    development of all Vatsonga in the Southern African subcontinent (region), and indeed the whole
                    world.
                </p>

                <p>
                    We have acknowledged with concern the recent developments in the Malamulele area and we feel that we
                    cannot remain aloof when the rights of our people are trampled upon and the will of the affected
                    people, not the wishes of their political masters that are paramount in determining the discourse
                    and direction to be taken. Indeed, after making their grievances known, the people of Malamulele
                    must surely be wondering about the following:

                <ol>
                    <li>
                        Who in fact is governing their area if they, the people and their wishes are not taken into
                        consideration when the decision affecting their daily lives are taken?
                    </li>
                    <li>
                        Who represents them and their interests when in the decision making process after realizing that
                        the voices of their elected representatives (e.g. Task Team) are either muted, ignored or absent
                        in this whole demarcation upheaval.
                    </li>
                    <li>
                        Whether they are taken seriously since successive delegations representing local, regional
                        (district), provincial and national government structures have visited the area whenever
                        tensions flare up and yet each contingent seems to be on a new fact-finding mission every time,
                        meaning either the previous ones never reported or it was a window-dressing exercise.
                    </li>
                </ol>

                <center><img src="../img/malamulele-within-thulamela-1000x678.jpg" class="img-responsive"></center>

                <b>The issue of tribalism with regard to Malamulele municipal demands</b>

                <p>
                    It should be noted that Malamulele residents are not tribalist as reported by those who are against
                    their demand, but had been victims of tribalism emanating from local government administration and
                    demarcation board proceedings which allowed the following factors stated below to develop unequal
                    socia-economic and unjust social justice. It is of great importance to also note that the area of
                    Malamulele incorporates both Tshivenda and Xitsonga speakers, and the twosome have been neighbouring
                    and staying together for years and involving in inter-marriages. Another crucial point that
                    distances the Malamulele Community from tribalism is that almost 80% if not 90% of posts, especially
                    in government institutions like both primary and secondary schools are occupied by Tshivenda
                    speakers whom neither reported any tribal attack nor incident from Malamulele Community members
                    since they arrived.
                </p>

                <ol>

                    <li><b>Socio-economic factors</b></li>
                    Between Malamulele and Thohoyandou, the Thulamela municipality dismally failed for 14 years to
                    accurately provide essential services to Malamulele which rates the largest population than its
                    counterpart, Thohoyandou, that continuously receives quality services from the very same local
                    municipality. This local municipality has been infested with a culture of marginalization in which
                    Malamulele has been subjected to notice this culture folding for years. This means that Thulamela
                    Local Municipality has created social and economic inequality in the provision of municipal services
                    and the sharing of resources. This social inequality alarmed the people of Malamulele to suspect
                    that Thulamela provides services alongside tribal affiliation where the Vatsonga at Malamulele have
                    been reduced to stand by and kept fully dependent on Thohoyandou which is predominantly occupied by
                    their Vhavenda counterparts, where the massive developments and economic boom take place. When
                    Malamulele people evinced this marginalization they are being ignored by local government
                    administrations and deemed as tribalist whereas they are victims of tribalism.

                    <li><b>Social justice</b></li>
                    Malamulele’s demand for a municipality is based on the socio-economic needs of the people of the
                    area. Being guided by the Municipal Demarcation Board’s response to Malamulele Demarcation Task
                    Team’s demands, we therefore found that the demarcation board had used infrastructure as weapon to
                    marginalize Malamulele in terms of municipal office location and deprived economic development as
                    justifications to decline Malamulele’s demand for their constitutional rights of own standing
                    municipality.

                </ol>
                <p>
                    We urge the Demarcation Board and the government to turn an eyelid on reports and allegations being
                    made by Malamulele Citizens against Thulamela Local Municipality, especially on the line of tribal
                    bias by the municipal officials. If these allegations are not attended to, the scenario will get
                    worse than those damages inflicted by the Apartheid regime. We also advice the Demarcation Board
                    that their decision in favour of Malamulele’s demand will help in the state’s financial
                    over-spending being used to pay and enrich those Public Order police officers’ overtime salaries
                    since they come from as far as Ulundi and New Castle in KZN, Grobblersdal, Hazyview and all corners
                    of the country, “may be the national army is the next”. The money that is being used to pay these
                    police men and women can be used to change or make difference in the lives of Malamulele citizens.
                </p>

                <p>
                    We therefore plead with the Municipal Demarcation Board and the government to immediately
                    resolve the disputes within Thulamela Local Municipality and to consider the granting of an
                    independent local municipality to the people of Malamulele as it is their constitutional right.
                </p>

