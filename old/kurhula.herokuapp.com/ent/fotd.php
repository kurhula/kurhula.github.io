                    Tonga, Thonga or Tsonga people and languages span most of southern Africa, notable countries being
                    South Africa, Swaziland, Mozambique, Malawi, Zambia and Zimbabwe. In these countries, there are
                    regions where one or more languages and/or dialects are more dominant. For example, in South Africa,
                    Tsonga people are mainly found in the Limpopo, Mpumalanga, KwaZulu-Natal and Gauteng provinces, with
                    smaller populations in North-West and Free State. Within these provinces, there are towns and cities
                    where they are most prevalent, although this is continually changing in the new South Africa as
                    black people can now move freely. Most or all of southern Mozambique is inhabited by Tsonga people,
                    variously named as Copi, Rhonga, Ndzawu, Tonga, Shangana, and Tshwa. The provinces are Maputo,
                    Maputo City, Inhambane, Manica, Gaza, and Sofala.
                </p>

