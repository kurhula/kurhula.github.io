                    Zimbabwe, Mozambique, South Africa and Swaziland. The Ngoni group involved was the
                    Ndwandwe-Gaza-Nguni, who spoke Lala, now a dialect of Zulu. As an estimate, the Shangaan dialect is
                    over 95% Tsonga.
                </p>

