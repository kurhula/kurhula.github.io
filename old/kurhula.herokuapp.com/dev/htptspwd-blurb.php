        I started preparing to join the Datawinners team while I was still in Braamfontein, Johannesburg.
        The team is based out of Koramangala, Bengaluru. I found that it was not easy to get started.
        Although I had my prospective team helping me setup from all the way in Bangalore, I found that I
        lacked proper understanding as to why we needed this package, this version of that package, this OS,
        Java on a Python project, etc. I felt that had a detailed README document been prepared, I would
        have navigated my way better. What could have also helped is an architectural diagram showing how
        things were connected. But I did not have this luxury. For a small team of 6, somehow I understood
        why the team never invested in documentation. Afterall, we did not have any open source contributors
        in spite of the project being of open source nature.
