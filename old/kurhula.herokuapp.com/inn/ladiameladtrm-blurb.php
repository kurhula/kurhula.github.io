    <h2 class="title-post">My Experiences Localising and Digitising Tswa-Ronga Material</h2>

    <p class="date">Last updated on: September 3, 2013 by <a href="../index.php#about">Musa Kurhula Baloyi</a></p>

    <p>
        This is a story about the day it all changed for me, and hopefully for other African and minority
        language speakers. It is a story about the day I had my “stroke of genius”. However, there was
        nothing particularly genius about this idea. It was all common sense, which, until this day, was not
        so common thanks to decades, perhaps centuries, of thinking in a particularly constrained
        manner.
    </p>

    <img src="../img/ladia1.png" class="img-responsive">

    <p>
        I was upset when I read the comment; No-one has the right to tell me what language to speak! But the
        conversation drove me to perform a quick Google search for an online Tsonga dictionary. To my
        surprise, no online dictionary existed! Instead I found dictionaries for almost all the other South
        African languages, and, if a dictionary or translator did not exist at the time, it was in the
        works.
    </p>

    <p>
        In South Africa, there is a perception that all Tsonga people are from Mozambique, so they are
        excluded from most national services such as TV broadcasting. But Mozambique is not so keen on
        preserving, let alone developing, native languages. Although Zimbabwe and Swaziland are active in
        language preservation, Tsonga is a minority there, so no African governments are actively trying to
        preserve the language. I found websites claiming to offer translation services, but they were just
        that, claims.
    </p>

