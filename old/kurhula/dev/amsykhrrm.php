                <p>
                    Agile Manifesto yi tsariwile hi vatsari vo ringana khume-mune: Mike Beedle, Arie van Bennekum,
                    Alistair Cockburn, Ward Cunningham, Martin Fowler, Jim Highsmith, Andrew Hunt, Ron Jeffries, Jon
                    Kern, Brian Marick, Robert C. Martin, Ken Schwaber, Jeff Sutherland na Dave Thomas. A ku ri lembe ra
                    2001 hi Nyenyanyani.
                </p>

                <p>
                    Hi Xitsonga, agile manifesto hi nga ku i “swikoxo swa matirhelo yo hatlisa no olova”, ku ya hi
                    muhundzuluxi, tatana Musa Kurhula Baloyi. Tatana Baloyi va tirha eka company yin’we na van’wana va
                    vatsari, ku nga vatatana Martin Fowler na Jim Highsmith. Muthori wa vona i ThoughtWorks. Tsalwa leri
                    ri ni nkoka swinene eka ku tsala ti-program ta computer (software), hambileswi ri nga tirhiki kona
                    ntsena.
                </p>

                <p>
                    Khaleni ka khaleni, ku tlula khume ra malembe la ma nga hundza, software a yi kunguhatiwa nkarhi wo
                    leha, tin’wheti kumbe malembe (“requirements gathering”). Se loko va hetile a va hlela makungu, va
                    ma xopaxopa (“analysis”). Kutani va ma andlala hi ku tirhisa swifaniso (“design”). Emakumu ku
                    ngheniwa ehansi ku mpfampfarhutiwa code (marito lama twisisiwaka hi computer, ma yi byela leswaku yi
                    endla yini). Xiphemu lexi xi vitaniwa “implementation”. Matirhelo lawa hi ku angarhela ma vitaniwa
                    “waterfall model”.
                </p>

                <p>
                    Tintlhari leta khume-mune leti nga tsariwa laha henhla ti swi vonile leswaku matirhelo lama ma nga
                    antswisiwa. Leswi xikongomelo-nkulu ku nga ku endla software, ha yini hi nga swi hatliseri hi endla
                    yona? A swi nga tsakisi ku kuma ku u lava ku cinca xo karhi kambe swi teka malembe na malembe hikuva
                    ku cinca lokuntshwa ku fanela ku famba hi nawu. Nkarhi wun’wana vatsari va software (ti-programmer)
                    a va heta ku tsala software se ntirho wa yona wu hundzeriwile, ivi yi cukumetiwa kumbe vini va yona
                    (ku nga van’wamabindzu) va boheka ku yi tirhisa hambi yi nga ha va tirheli. Xo vava ngopfu hiloko va
                    tsandzeka ku heta ku yi vumba hi nkarhi hikwalaho ka swin’wana na swin’wana, xik., ku hela ka mali
                    yo hakela vatirhi. Va va va lahlekeriwe ngopfu.
                </p>

                <p>
                    Masiku lawa, swi teka mavhiki ma nga ri mangani ku endla mintirho leya mune (requirements gathering,
                    analysis, design na implementation) kumbe ku tlula. Hi ndlela leyi, ku cinca ku ta hi ku olova.
                    Van’wamabindzu va kota ku vona leswaku software ya vona yi ta tirha mayelana na yini loko yi herile.
                    Na loko swikwhama swo tshuka swi boxekile, vana xo xi komba. Mbuyelo lowu wa kumeka hikuva ntirho
                    lowukulu wa tsemiwa wu hundzuka swintirhwana leswi hi xin’we-xin’we swi kotekaka hi xinkadyana.
                </p>

                <p>
                    Matirhelo yo Olova no Hatlisa ya na swikoxo, ivi swi landzeriwa hi milawu. Swikoxo i nkomiso wa
