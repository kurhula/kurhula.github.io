                    landzelelaka siku na siku, va tlhela va yi tirhisa ku vona leswaku va endla swona xana.
                    Tsalwa ra matirhelo lawa ri hundzuluxiwile ri ya eka ririmi ra Xitsonga, k’endlela leswaku vo tala
