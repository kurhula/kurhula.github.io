    <h1 class="title-post"><a href="dev/amsykhrrm.php">Agile Manifesto se ya Kumeka hi Ririmi ra Manana</a></h1>

    <p class="date">Last updated: July 2014 by <a href="../index.php#about">Musa Kurhula Baloyi</a></p>

    <p>
        Agile Manifesto yi tsariwile hi vatsari vo ringana khume-mune: Mike Beedle, Arie van Bennekum,
        Alistair Cockburn, Ward Cunningham, Martin Fowler, Jim Highsmith, Andrew Hunt, Ron Jeffries, Jon
        Kern, Brian Marick, Robert C. Martin, Ken Schwaber, Jeff Sutherland na Dave Thomas. A ku ri lembe ra
        2001 hi Nyenyanyani.
    </p>

    <p>
        Hi Xitsonga, agile manifesto hi nga ku i “swikoxo swa matirhelo yo hatlisa no olova”, ku ya hi
        muhundzuluxi, tatana Musa Kurhula Baloyi. Tatana Baloyi va tirha eka company yin’we na van’wana va
        vatsari, ku nga vatatana Martin Fowler na Jim Highsmith. Muthori wa vona i ThoughtWorks. Tsalwa leri
        ri ni nkoka swinene eka ku tsala ti-program ta computer (software), hambileswi ri nga tirhiki kona
        ntsena.
    </p>

    <p>
        Khaleni ka khaleni, ku tlula khume ra malembe la ma nga hundza, software a yi kunguhatiwa nkarhi wo
        leha, tin’wheti kumbe malembe (“requirements gathering”). Se loko va hetile a va hlela makungu, va
        ma xopaxopa (“analysis”). Kutani va ma andlala hi ku tirhisa swifaniso (“design”). Emakumu ku
        ngheniwa ehansi ku mpfampfarhutiwa code (marito lama twisisiwaka hi computer, ma yi byela leswaku yi
        endla yini). Xiphemu lexi xi vitaniwa “implementation”. Matirhelo lawa hi ku angarhela ma vitaniwa
        “waterfall model”.
    </p>
