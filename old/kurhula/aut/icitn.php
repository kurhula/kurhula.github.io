                <h1 class="title-post">I Cannot Ignore the News</h1>

                <p class="date">Last updated on: January, 2007 by <a href="../index.php#about">Bantsi Makhandeni</a></p>
                <br/>

                <p>
                    Our church is always full. Today was no exception. We praise the Lord at the University of the
                    North’s KB Auditorium everyday from Saturday to Sunday. On any given weekday we worship for an hour
                    and a half, from 6 till half 7. Saturdays, however, are reserved for the church choir. Members
                    converge to practice songs that are rendered on the other days of the week.
                </p>

                <p>
                    The service would proceed as usual. And the agenda would look something like this:
                </p>

                <p>
                    We will open by prayer, sing a few choruses, announcements made by secretary, envelopes circulated
                    for the sacred purpose of tithing, the pastor will minister, gifts will be collected, church choir
                    will bless us and we shall close by prayer. The concerned MC will guide us through.
                </p>

                <p>
                    Inside the church hall and outside are two contradicting worlds synonymous to heaven and hell. When
                    you are inside you accept God anew, every day. Amen to that! Once you are in the outside world, you
                    become the devil himself, you have always been.
                </p>

                <p>
                    Looking around I can see churchgoers leaving the temple. Not a single soul ever wanted the sermon to
                    be over. But now that it was, they seem to be in such a hurry. Some rush to their dormitories to
                    make their tables and dine. Some rush to their quarters to make friends with their books all over
                    again. Some do things I’d never know behind closed doors.
                </p>

                <p>
                    While looking around, I finally find what I was looking for. She is also a staunch member of the SCO
                    (Students’ Christian Organization). Her name was Thando. She’s a Swati who hails from Swaziland, a
                    country inside another country, called an inland country. In South Africa, her home is Likazi, a
                    town in Mpumalanga Province. Every chance she gets, she visits her family eSwazini.
                </p>

                <p>
                    I cannot talk about Thando without mentioning Lira. ‘Lira’ is derived from Lerato, meaning love,
                    uthando. There was not Thando without Lira. And, for what it’s worth, there was no Lira without
                    Thando. Wherever you saw one, you saw the other. Lira is a diehard Tswana girl who, for all those
                    years at Turfloop, speaks deep and pure Setswana. Actually, this was her second year in the
                    institution. While in Hammanskraal, Lira never thought much of her mother tongue. I can only say
                    that she changed for the better.
                </p>

                <p>
                    It took me a year to get Thando to notice my presence. That was in 2004. last year she and her
                    friend were talking to Echo and Costa. Both guys came from rich families and consequently had all
                    the girls in the class of two thou four drooling over them. We were doing Media Studies; me, Thando,
                    Lira, Echo and Costa, and other students that I haven’t introduced yet. However, because things came
                    too easy to both Echo and Costa, it was hard to value anything. Echo would chat up a girl in front
                    of Thando without feeling guilty about it. This hurt me so bad. The girl that I loved was suffering
                    under him. She deserved to be treated better. But I knew I could never get her. Fannie agrees.
                </p>

                <p>
                    Fannie was interested in Lira. They’ve always been talking to each other ever since we got to Turf.
                    Fannie was full of jokes whereas Lira was a very serious person. He got to her comically, like he
                    didn’t mean to talk to her. Just like another one of his silly jokes. Even Lira’s brilliant mind
                    couldn’t see through him. He hid his own self in the jokes that he cracked. But they were always
                    quarrelling. Lira didn’t find his jokes funny and Fannie couldn’t be serious, no matter how hard he
                    tried. So he laughed at his own jokes out of misery. If only they could understand each other, their
                    combination would be superb. One would help the other let her hair down and the other would remind
                    the other that the world is not all roses - in the same breath, there are lilies.
                </p>

                <p>
                    With Thando around, Lira could smile and laugh, and even tell jokes. Thando was one link between me
                    and Lira. The other was Fannie. With Fannie around, she could not smile or laugh, let alone tell a
                    joke. With both Thando and Fannie around, she was a time bomb. She could laugh any minute. She could
                    get aggressive just as quickly. We had to be cautious and mind her feelings in our conversations.
                </p>

                <p>
                    At work it was as if we had stopped being friends. We became colleagues. Most writers in the gazette
                    were doing their final level. “They need the experience,” Mrs. Kwenaite would say when she turned
                    freshmen and sophomores down. My friends and I were lucky to get the job at second year. Other
                    employees chose not to think of it as ‘lucky’. Mrs. Kwenaite, our manager, was as strict as they
                    get. If she spotted you dilly-dallying, you’d not only have to answer to her, you’d feel her wrath
                    too. That was true for everyone, from cartoonist to editor.
                </p>

                <p>
                    Some guy called Kwena was our editor. He was in the same third-year class as Ro. Why Ro? Everybody
                    knew Ro. Ro was living like a celeb with a brush, big earrings and full red lips. She was portrayed
                    like that in the paper. All the gos came from Ro. “It’s gos and it’s Ro,” was the phrase that pays.
                    She did not care who you were. If the campus was talking, you’d see yourself in the weekly. Those
                    who loved her called her “Mhani N’wa-Tsolo” (a big liar). And those that hated her had to call her
                    Mhani N’wa-Tsolo.
                </p>

                <p>
                    Now back to our subject. There was something going on between Kwena and Thando. This ‘fling’ reached
                    a point where I could not tolerate it anymore. My resolution was to dump her in front of him. It
                    worked wonders. Problem was we were at Turfloop Now and personal relationships were forbidden. Mrs.
                    Kwenaite invited the three of us to her office where she reminded us of some work ethics. She made
                    Thando recite them loudly from the wall in front of her two boyfriends. Activity stopped. People
                    were listening to a good show. Good, but embarrassing at the same time.
                </p>

                <p>
                    She took off here goggles and wiped them with a cloth. Not that they were blurring her vision or
                    anything. She put them back on. With the free hands, she contemplated what she was going to do with
                    us. Those hands could be me and Kwena. She looked at them and wound up holding them together. No,
                    that was not me and Kwena. We were too far from being close.
                </p>

                <p>
                    After letting us feel stupid for a long while, biting our nails and swallowing our saliva, she
                    opened her mouth to speak. I stretched my hands and kicked my legs. Standing for thirty minutes up
                    would get anyone tired of standing. “You don’t bring your dirty linen to the office and wash them
                    here. Once you are inside Turfloop Now, you respect the brand and protect it. If not, I will protect
                    it from people like you. What transpired on this day the 20th August 2005 among your TRIUMVIRATE is
                    history. I will not hear it again. You two lovebirds may be excused. Kwena, remain.” Ahehe!
                </p>

                <p>
                    The structure of Turf Now is different from that of the Capricon Voice or any other paper in the
                    real world. It was run by a University with the sole purpose of giving its learners some hands-on
                    experience in industry. An editor here did not have as much authority as an editor elsewhere.
                </p>

                <p>
                    When I was still inside, I caught her fury. That made it look like I was the most frightened. I am
                    quick to catch anything. When I’m around the girls, I tend to be an abridged version of a girl. When
                    I’m with Fannie, I become funny too. Now that I was around people who were busy working, I got down
                    to business. It was as if I was dynamic and everyone was contagious. Because of this, I did not get
                    a column in the paper. It would be hard for me to be consistent or keep a trademark. My job was on
                    the streets where things were always changing.
                </p>

                <p>
                    You will wonder what kind of business I immersed myself in since my job was on the streets. I was
                    helping Lira proofread the paper that was due in the next publication. All articles had already
                    passed through the editor’s hands. She was quick to think and she was accurate. Therefore, the
                    position was perfect for her. In the event that there is a cash-in-transit heist in Polokwane,
                    they’d send me to cover the story. If somebody else needed assistance, I’d be man for the job, even
                    if I did not know it.
                </p>

                <p>
                    It was after this incident that we became estranged from each other, me and Thando. I did not care.
                    She was a cheater. As a result, I had more time in my hands to study. I did not run wild trying to
                    replace her. Lira would come to my room if I needed her help. I could not go to hers. Lira is
                    roommates with Thando, remember? I did not want to see Thando. Neither did she want to see me.
                    Fannie would turn up unannounced and find us studying. I’d be sitting on the chair and Thando would
                    be on my bed with a duvet. He did not like it. But there was only one chair, which I was sitting on.
                    What could he do? He hid his dissatisfaction in jokes. Still, Lira could not laugh at them. Fannie,
                    on the other hand, opted to stand on his feet throughout his stay.
                </p>

                <p>
                    “In Simply Fannie (the name of his feature) I’ll write about madmen”, he uttered. “It was me, you
                    Chilli and Thando. We were escaping from Khensani Hospital for the Mentally Handicapped. I said “If
                    we find a long fence, we’ll dig under it.” Thando said “If we find a short fence, we’ll climb over
                    it.” And then we set off. When we got there, you said “Tough luck! We must go back. There’s no
                    fence”.” I thought that was simply funny.
                </p>

                <p>
                    “And there was this other time when Thando nearly drowned in the pool. The instructor sent for you
                    and he said “I have good news and bad news. I will start with the good news. We thank you for saving
                    our student from drowning. Unfortunately she hung herself on the washing line.” So Chilli Boy said
                    “No. She did not hang herself, I put her to dry”.”
                </p>

                <p>
                    The jokes had become too much. But he said he had only one left. I took his word for it. “You came
                    home from the pub and lay down beside Thando. But you realized that there was a third guy in the
                    same bed. You asked her “Who’s sleeping with us?” She said “No-one”. “But there’s three of us in the
                    bed.” “No, we’re only two. Climb down and count.” You got off and started counting. “One, two. Oh!
                    You were right.” And then you went back to bed.”
                </p>

                <p>

                    He was using all the wrong names. It was as if he was sending a message to Lira. That no matter what
                    happened, Thando and I belonged together. Again, it was as if he was asking me how I’d feel if I
                    bust Thando in bed with another man. He bid farewell when we started talking books, pretending not
                    to know anything other than his back page column at the paper. But I knew him much too well.
                </p>

                <p>
                    Academically, I was becoming good myself. Good as to even help Lira with some sections in the
                    various courses. And when she said she was going to Ro’s for help, she was coming to my place. I was
                    becoming the man that Lira wanted, one who was intellectually challenging and serious. Before I knew
                    it, she had fallen in love with me. So I used her to get over Thando. When Thando was asking me
                    back, it hit me that I was over her. In fact I was in love with her best friend, room mate and
                    classmate. The one she was always seen with.
                </p>

                <p>
                    The verdict was to take her back and shield her from what was happening between me and Lira. Lira
                    would now pretend to be so into Fannie. We were too deep in love to stop, we could only hide it. My
                    knowledge of Media Studies was reaching new frontiers, but my knowledge of Bible Studies was going
                    down the drain.
                </p>

