                <p>
                    Next Sunday…
                </p>

                <p>
                    “Girls!” I say, cheerfully. “Hey!” they reply, simultaneously. We hug. I start with my official
                    girlfriend Thando. She meets me halfway. We hug. Then I get closer to my secret lover Lira. She lets
                    me come to her. We hug. Thando does not think anything of this. Life at varsity was westernized.
                </p>

                <p>
                    “So what’s the latest?” asked Thando. “I read in Turfloop Now that we’re closing next week,” I
                    respond. “Huh-huh!” that’s Thando. “I heard…” Lira comes in. “You heard what?” asks Thando
                    impatiently. “I heard…I heard…I heard.” The first one was louder, like it was breaking news. The
                    second one was lower, like it was sad news. The last one was lowly, like that was the news.
                </p>

                <p>
                    Monday, after lectures…
                </p>

                <p>
                    Tonight is a school night. We have work to do. The Literature lecturer is nuts. He can give us a
                    quiz without prior warning. There’s an English test tomorrow and Journalism practical assignment due
                    on Wednesday. Our schedules are busy so I’m going straight to my room.
                </p>

                <p>
                    Before I could say bye, Thando interrupts. “Chilli, are you coming in with us?” I’ve always had a
                    problem with this chick. I could never resist her. “If a full-body massage is on the menu!” She does
                    not say anything but gives me the love look.
                </p>

                <p>
                    They stay at MBL North, room number 0011 on the ground floor. Originally this was Thando and
                    Shirley’s room before Shirley moved out to stay with her boyfriend and Lira moved in with Thando.
                    Lira’s room now belongs to Ro. I never knew her real name, maybe it is Ro. Every now and then Lira
                    visits Ro, more especially when her grades are sinking. Ro is a bookworm, and so was Lira, before
                    she met Thando and entered the boy zone.
                </p>

                <p>
                    It’s only one in the afternoon. Maybe I’ll stay until three. Time flies when you are having a good
                    time. Before I knew it my phone was saying 10 pm. Very few people on campus had clocks on their
                    walls or watches on their wrists. Forget the stats, it shall be midnight in two hours and I have a
                    surprise quiz to prepare for. “Bo-nice, it’s been nice. But a brother gots to go,” I tell this to
                    Lira. I know she’ll let me go. “You ain’t gotta bounce,” answers the wrong person.
                </p>

                <p>
                    What am I to do? “Don’t do that!” but she doesn’t stop. “Please!” Thando can be stubborn, and I,
                    well, I find that debilitating. She seduces me until we’re behind the curtain and Lira can’t see us
                    anymore. The raffle of paper begins. Lira is studying. I should be doing likewise, if Thando was not
                    in her white gown. “Thando, we’re writing a quiz tomorrow.” “Nobody knows for sure,” and she starts
                    her magic. I could get used to this.
                </p>

                <p>
                    Tuesday caught me off guard. At the end of the day, I switched off my phone and buried my sorrows in
                    my books. It’s a test tomorrow and I can’t afford to fail it.
                </p>

                <p>
                    “E jo, wake up. It’s getting late.” Damn, I nearly overslept. “E jo, what time is it?” “Thirty
                    minutes before you sit for the test.” Quickly I rush to the shower. The water is ice cold. Without
                    finishing the job, I’m out. I was used to this. You know, crossing the night. These were the
                    repercussions.
                </p>

                <p>
                    “Had an orgy last night, Chilli Boy?” jokes Fannie. “With a record four chicks!” I say, panting like
                    a wild dog. Fannie sits in front of me because his surname comes before mine when arranged in
                    alphabetical order. He turns around and whispers “Let me guess, with your girlfriend and her three
                    godmothers.” The people around us hear us, but they know the drill.
                </p>

                <p>
                    Finally, I’m finished writing. That was easy. I swear I’ll prepare in time next time, and this time
                    I’m for real. Do not mark my words.
                </p>

                <p>
                    Lira is leaning against a wall opposite the exam hall. I’m not surprised she finished before all of
                    us. Fannie and Thando will be out in no time. No matter how different our “study” methods, we
                    finished roughly the same time. Thando was so used to writing her notes on the ruler, and anything
                    she could get her hands on. It was impossible for her to study. Fannie studied in secret. He hid
                    everything.
                </p>

                <p>
                    The only skeleton that I kept in my closet was my love affair with Lira. Though Lira was not as
                    beautiful as Thando was, Lira could think. I liked her for that. She could think about her life, her
                    environment, her future, and what not. Thando was about the here and the now. But I liked her
                    because she was beautiful and attractive. I could never get my eyes off her.
                </p>

                <p>
                    Most people couldn’t get their eyes off her. As long as you wanted Thando, she wanted you too, rich
                    or broke, handsome or ugly, student or lecturer. Echo was lucky because she wanted him more than he
                    wanted her. If the tables had turned before he deregistered, I would’ve seen her for who she was.
                    And maybe I would’ve chosen Lira over her. But I’d have to get past Fannie, now that Costa had moved
                    on with Ro. Not to worry, I’d cross that bridge when I got to it.
                </p>

                <p>
                    “Hey, sexy! How did it go?” “You slept with Thando while I listened to your moaning.” I tried to
                    explain that maybe it was a trick. Maybe she heard something and wanted to check if it was true. If
                    I had refused, she’d know. Of course I didn’t think of this when we made sweet love in front of my
                    secret lover. “I’m sorry.” I said in the end. We agreed that Friday was ours. Plans were made. Lira
                    would tell Thando that she was sleeping at Ro’s. I’d kick my room mate out for the night. Send him
                    to Fannie’s place. Fannie had a single room.
                </p>

                <p>
                    Thando and Fannie came together to where we were standing. He was cracking her up like usual. Unlike
                    Lira, she found his jokes hilarious. When they got there, Friday night had been dissected to cell
                    level.
                </p>

                <p>
                    So what’s the news? Ever seen writers that don’t like a juicy story? All the way to our hostels we
                    spoke about things that were happening around us. But nobody dared raise a topic that penetrated our
                    boundaries.
                </p>

                <p>
                    Friday came…
                </p>

                <p>
                    “E jo!” says my room mate. “I know I was meant to sleep at Fannie’s, but Fannie’s got a girl. Come
                    on out and I’ll tell you about it.” Lira covered herself up and I opened the door. Part of my torso
                    was on the inside. It was the head that had popped out. “Her name is Thando. I believe that’s your
                    Thando.”
                </p>

                <p>
                    This would be a scoop for Ro if she found out. She’d be willing to put Lira in her place. They’ve
                    been the worst of enemies ever since Ro made up with Costa. Ro had the perception that Lira wants
                    him back. She couldn’t be more wrong. Costa was all cash and no brains. But I wouldn’t know.
                </p>

                <p>
                    “How could we be so stupid?” said Lira when I got back and related the story to her. “I mean, me and
                    Ro are not talking. Everybody knows that. But I lied about sleeping at hers. I know it’s my room too
                    and don’t need her permission, but it does not make any sense.” There was no need to think long and
                    hard. Precious milk had split. “Technically it wouldn’t mean that I’m with you, even if I’m not with
                    Fannie. But Thando knows everybody that I talk to. If there was a guy in my life, sure I’d tell her.
                    But if that guy was you, I’d do everything in my power to hide it.”
                </p>

                <p>
                    This got me thinking. For how long has she known? For how long has she been sleeping with Fannie
                    behind my back? How many jokes did they crack? Bet when she was laughing she was thinking, ‘they
                    think they are the smart ones.’
                </p>

                <p>
                    I cannot ignore the news. God forgive me for what I’m about to do. I fail to ignore the news.
                    Something has to be done about it. But we also have to be smart about it. I said thanks to rumza
                    before he left. One more favour to ask from him. “Hello. Could you sleep here for tonight?” “Where
                    are you going to sleep?” “Out.” “Out where?” “I can’t tell you where. That’s classified information.
                    You’ll definitely read it in the paper. I’m sure Ro won’t miss it for the world.” “Just lock the
                    door and switch off the lights. I will come later.”
                </p>

                <p>
                    There was a sudden change of plans – Thando and I were going to finish what we had started at MBL
                    North 0011, Lira’s place.
                </p>

                <p>
                    Shirley came knocking at dawn. They just had a fight with her boyfriend. We let her talk to the
                    hand. We needed our sleep for we had not slept that night. She fell asleep on Thando’s bedside with
                    her mouth open and tears in her eyes.
                </p>

                <p>
                    When Thando got there in the morning, I was having breakfast in bed. Lira and I in the same bed was
                    the first thing she saw. As you know, Lira owned the front room. “You can join us or you can go
                    comfort Shirley across the curtain. Poor thing! She has just broken up with her boyfriend.” It felt
                    right to say that to her face.
                </p>
