                <p>
                    <strong>Malamulele Revolts</strong>
                </p>

                <p>
                    From the day when Malamulele became administered in Thohoyandou, to the day when the people of
                    Malamulele took to the streets, a lot of politics happened. In 2010, Malamulele started making the
                    news for all the wrong reasons: barricading roads, keeping kids out of school, setting government
                    property alight, burning 4 to 5 schools. The whole of South Africa weighed in reprimanding the
                    people of Malamulele online and in public conversations. The national ANC government and the
                    Presidency made public statements about Malamulele not being viable and the protests being based on
                    tribal hatred. The Thulamela local municipality made claims about having provided equal services to
                    Thohoyandou and Malamulele. The Municipal Demarcation Board echoed Thulamela and the ANC, even
                    releasing a feasibility report criticised of being too hasty. The Malamulele Task Team, the
                    successor of Malamulele SANCO which had failed at the same task, was relentless. More news.</p>

                <p>
                    Malamulele was compared to other areas in the country going through service delivery protests, such
                    as Bekkersdal and Khutsong. President Jacob Zuma said the people of Malamulele needed prayers.
                    Cooperative Governance and Traditional Affairs Minister Richard Baloyi went to listen to the
                    people's grievances and promised to come back but never did. His successor Pravin Gordhan lamented
                    not having influence over Chapter 9 institutions such as the MDB. This at a time when the ANC was
                    undermining the Public Protector's office led by Thuli Madonsela. The people of Malamulele were
                    baying for Malamulele-born Collins Chabane's blood, his family had to flee from Xikundu. The DA,
                    which had won 6% of the vote in Malamulele in the previous local government elections, similar to
                    the EFF, weighed in. The EFF "government-in-waiting" committed to declaring Malamulele a
                    municipality. Even more news.
                </p>

                <p>
                    <strong>Malamulele and Vuwani</strong>
                </p>

                <p>
                    In a 360&deg; about-turn, the MDB wants to create a municipality in Malamulele, incorporating a
                    smaller area called Vuwani. The ANC wants to name it the Collins Chabane Municipality in honour of
                    the fallen freedom fighter. Chief Mbangiseni Masia says none of the Venda chiefs in Malamulele and
                    Vuwani wants any part of this new municipality. More protests. More politics.
                </p>
