                <p>
                    <strong>
                        The Death of Gazankulu
                    </strong>
                </p>

                <p>
                    The years 1990 - 1993 were a period of transition, where the National Party (led by Frederik Willem
                    de Klerk) and the ANC (led by Nelson Rolihlahla Mandela) shared power. In 1994, the Gazankulu and
                    all homeland governments were dissolved. That meant the map of South Africa needed to be redrawn.
                    The homeland or bantustan system was created based on language. Except in one case where the Xhosa
                    had two homelands, every other indigenous South African language was assigned a homeland. So in 1994
                    the first-ever democratically-elected president in South Africa was ushered in. </p>

                <p>
                    <strong>The Birth of Thulamela</strong>
                </p>
                <p>
                    Between 1994 and 1999, Malamulele was part of the Levubu-Shingwedzi Transitional Local Municipality.
                    This municipality incorporated all areas east of Ribvubye, which included some areas led by
                    Tshivenda-speaking chiefs. Malamulele is Xitsonga-speaking. Between 1999 and 2001, the
                    Levubu-Shingwedzi Transitional Local Municipality was joined with the Thohoyandou Transitional Local
                    Municipality. Thohoyandou used to be the capital of the Venda homeland, predominantly west of
                    Ribvubye.</p>

                <p>
                    Many chiefs, coucillors, and residents from Malamulele were against this move. The people of
                    Malamulele had been used to Malamulele Town being the center of their administration since the
                    1970's, that's around 30 years. Now all that was about to change. Thohoyandou was more developed
                    than Malamulele owing to having been the capital of Venda, but Malamulele was not that worse off.
                    Malamulele could handle almost all economic activities.</p>

                <p>
                    However, the ANC government with their Municipal Demarcation Board, were more concerned about ending
                    the apparent tribalism caused by the Apartheid government. Merging communities that spoke different
                    languages was part of the process of healing and erasing Apartheid's legacy in the new South Africa.
                    And Malamulele was their guinea pig.</p>

                <center>
                    <img src="../img/tmc1.JPG" class="img-responsive">
